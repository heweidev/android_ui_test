        if (dy > 0 && mCurrentTargetOffsetTop > 0) {
            if (dy > mCurrentTargetOffsetTop) {
                consumed[1] = dy - (int) mCurrentTargetOffsetTop;
            } else {
                consumed[1] = dy;
            }
            mCurrentTargetOffsetTop -= consumed[1];
            offsetLoadingView(-consumed[1]);
            return;
        }

