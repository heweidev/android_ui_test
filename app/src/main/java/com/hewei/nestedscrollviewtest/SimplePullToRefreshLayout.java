    @Override
    public void onNestedScroll(final View target, final int dxConsumed, final int dyConsumed,
                               final int dxUnconsumed, final int dyUnconsumed) {
        // Dispatch up to the nested parent first
        dispatchNestedScroll(dxConsumed, dyConsumed, dxUnconsumed, dyUnconsumed,
                mParentOffsetInWindow);

        // This is a bit of a hack. Nested scrolling works from the bottom up, and as we are
        // sometimes between two nested scrolling views, we need a way to be able to know when any
        // nested scrolling parent has stopped handling events. We do that by using the
        // 'offset in window 'functionality to see if we have been moved from the event.
        // This is a decent indication of whether we should take over the event stream or not.
        final int dy = dyUnconsumed + mParentOffsetInWindow[1];
        if (dy < 0 && !canChildScrollUp()) {
            mTotalUnconsumed += Math.abs(dy);
            //moveSpinner(mTotalUnconsumed);
        }

        int offset;
        int contentTop = mCurrentTargetOffsetTop;
        int targetTop = contentTop - dyUnconsumed;
        if (dyUnconsumed < 0 && targetTop >= 0) {
            if (targetTop <= LOADING_VIEW_HEIGHT) {
                offset = -dyUnconsumed;
            } else {
                offset = LOADING_VIEW_HEIGHT - contentTop;
            }
            mCurrentTargetOffsetTop += offset;
            offsetLoadingView(offset);
        }
    }

